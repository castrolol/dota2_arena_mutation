function replaceNames(text: string) {

    const replaces = {
        "Shota-san": "Sr. Carlos",
        "Chiho-chan": "Srta. Samanta",
        "Ryunosuke-san": "Sr. Ricardo",
        "Shota": "Carlos",
        "Chiho": "Samanta",
        "Ryunosuke": "Ricardo",
    }

    let newText = text;

    for (let prop in replaces) {
        newText = newText.replaceAll(prop, replaces[prop]);
    }

    return newText

}

